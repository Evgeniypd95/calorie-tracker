const interpolate = (text, params) => {
  if (!params) return text;
  return text.replace(/\{(\w+)\}/g, (match, key) => {
    if (params[key] === undefined || params[key] === null) return match;
    return String(params[key]);
  });
};

const getNestedValue = (obj, path) => {
  return path.split('.').reduce((acc, key) => acc && acc[key], obj);
};

export const normalizeLocale = (locale) => {
  if (!locale) return DEFAULT_LOCALE;
  const normalized = locale.toLowerCase().split('-')[0];
  return SUPPORTED_LOCALES.includes(normalized) ? normalized : DEFAULT_LOCALE;
};

export const getDeviceLocale = () => {
  const locale =
    Localization.getLocales?.()[0]?.languageCode ||
    Localization.locale?.split('-')[0] ||
    DEFAULT_LOCALE;
  return normalizeLocale(locale);
};

let currentLocale = DEFAULT_LOCALE;

export const setLocale = (locale) => {
  currentLocale = normalizeLocale(locale);
};

export const getLocale = () => currentLocale;

export const getLocaleCode = (locale) => (normalizeLocale(locale) === 'ru' ? 'ru-RU' : 'en-US');

export const translate = (key, params, localeOverride) => {
  const locale = localeOverride || currentLocale || DEFAULT_LOCALE;
  const value =
    getNestedValue(translations[locale], key) ||
    getNestedValue(translations[DEFAULT_LOCALE], key) ||
    key;
  return interpolate(value, params);
};

export const t = (key, params) => translate(key, params, currentLocale);

export const getDayNameShort = (dayIndex, tFn = t) => {
  const keys = ['sun', 'mon', 'tue', 'wed', 'thu', 'fri', 'sat'];
  return tFn(`days.short.${keys[dayIndex]}`);
};

export const getMealTypeLabel = (mealType, tFn = t) => {
  const key = mealType ? mealType.toLowerCase() : '';
  if (!key) return mealType || '';
  return tFn(`mealTypes.${key}`);
};

export const getMealTypeLabelLower = (mealType, tFn = t) => {
  const label = getMealTypeLabel(mealType, tFn);
  return label.toLowerCase();
};

const LocalizationContext = createContext({
  locale: DEFAULT_LOCALE,
  localeCode: getLocaleCode(DEFAULT_LOCALE),
  t: (key, params) => translate(key, params, DEFAULT_LOCALE),
  setLocale: () => {}
});

export const LocalizationProvider = ({ children }) => {
  const [locale, setLocaleState] = useState(getDeviceLocale());

  setLocale(locale);

  const tWithLocale = useCallback((key, params) => translate(key, params, locale), [locale]);

  const value = useMemo(() => ({
    locale,
    localeCode: getLocaleCode(locale),
    t: tWithLocale,
    setLocale: (nextLocale) => setLocaleState(normalizeLocale(nextLocale))
  }), [locale, tWithLocale]);

  return (
    <LocalizationContext.Provider value={value}>
      {children}
    </LocalizationContext.Provider>
  );
};

export const useLocalization = () => useContext(LocalizationContext);
